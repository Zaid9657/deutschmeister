// Helper to get all topics for a level
export const getTopicsForLevel = (level) => {
  return grammarTopics[level] || [];
};

// Helper to get a specific topic
export const getTopic = (level, topicId) => {
  const topics = grammarTopics[level] || [];
  return topics.find(t => t.id === topicId);
};

// Helper to get topic by slug
export const getTopicBySlug = (level, slug) => {
  const topics = grammarTopics[level] || [];
  return topics.find(t => t.slug === slug);
};

// Get total grammar topics count
export const getTotalGrammarTopics = () => {
  return Object.values(grammarTopics).reduce((sum, topics) => sum + topics.length, 0);
};
